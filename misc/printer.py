class Logger():
    def __init__(self):
        pass

    def info(self, string):
        print("[INFO]  ==> " + string)

    def log(self, string):
        print("[LOG]   ==> " + string)

    def db(self, string):
        print("[DB]    ==> " + string)

    def reid(self, string):
        print("[REID]  ==> " + string)

    def color(self, string):
        print("[COLOR] ==> " + string)